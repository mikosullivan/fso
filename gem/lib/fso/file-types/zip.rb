class FSO::File::Zip < FSO::File
	#---------------------------------------------------------------------------
	# unzip
	#
	def unzip()
		cmd = FSO.paths['unzip'], path
		cap = EzCapture.new(*cmd)
		cap.raise_on_failure 'unzip-error'
		return FSO.pwd
	end
	#
	# unzip
	#---------------------------------------------------------------------------
end
